/**
 * Serialize a single `<BehaviorTree>` element (description comment + color comment + body).
 */
function serializeBehaviorTree(tree: TreeData): string {
  let xml = '';

  if (tree.description) {
    xml += `  <!-- ${escapeXML(tree.description)} -->\n`;
  }

  if (tree.color) {
    xml += `  <!-- BTstudio:color=${escapeXML(tree.color)} -->\n`;
  }

  xml += `  <BehaviorTree ID="${escapeXML(tree.id)}">\n`;
  const structure = buildTreeStructure(tree.nodes, tree.edges);
  xml += serializeNodeRecursive(structure.root, tree.nodes, tree.edges, tree.variables, 2);
  xml += '  </BehaviorTree>\n';

  return xml;
}

/**
 * Build a parent -> children map and locate the root node.
 */
function buildTreeStructure(
  nodes: AppNode[],
  edges: AppEdge[],
): { root: AppNode | null; childrenMap: Map<string, string[]> } {
  const root = nodes.find((n) => n.data?.category === 'root') ?? null;

  const childrenMap = new Map<string, string[]>();
  for (const edge of edges) {
    const children = childrenMap.get(edge.source) ?? [];
    children.push(edge.target);
    childrenMap.set(edge.source, children);
  }

  // Sort children by visual x-position (left to right)
  childrenMap.forEach((children, _parentId) => {
    children.sort((a, b) => {
      const ax = nodes.find((n) => n.id === a)?.position?.x ?? 0;
      const bx = nodes.find((n) => n.id === b)?.position?.x ?? 0;
      return ax - bx;
    });
  });

  return { root, childrenMap };
}

/**
 * Recursively serialize a node and its children to BT XML.
 *
 * The root node itself is transparent -- it serializes its single child
 * directly, because BehaviorTree.cpp trees always start below `<BehaviorTree>`.
 */
function serializeNodeRecursive(
  node: AppNode | null,
  nodes: AppNode[],
  edges: AppEdge[],
  variables: Variable[],
  indentLevel: number,
): string {
  if (!node) return '';

  const indent = '  '.repeat(indentLevel);
  const { data } = node;

  // Root is transparent: serialize its single child
  if (data?.category === 'root') {
    const children = getNodeChildren(node.id, nodes, edges);
    return children.length > 0
      ? serializeNodeRecursive(children[0], nodes, edges, variables, indentLevel)
      : '';
  }

  // SubTree nodes use a dedicated format
  if (data?.category === 'subtree') {
    return serializeSubTreeNode(node, indentLevel);
  }

  // --- Regular node ---

  const nodeType = data?.type ?? 'Unknown';
  // DeclareVariable is an editor-only concept; export as SetBlackboard
  const exportType = nodeType === 'DeclareVariable' ? 'SetBlackboard' : nodeType;

  const fields: NodeField[] = data?.fields ?? [];
  const nodeName: string | undefined = data?.nodeName;

  const attrs = buildAttributeString(fields, nodeName);
  const children = getNodeChildren(node.id, nodes, edges);

  if (children.length === 0) {
    return `${indent}<${exportType}${attrs}/>\n`;
  }

  let xml = `${indent}<${exportType}${attrs}>\n`;
  for (const child of children) {
    xml += serializeNodeRecursive(child, nodes, edges, variables, indentLevel + 1);
  }
  xml += `${indent}</${exportType}>\n`;
  return xml;
}

/** Serialize a SubTree reference node (always self-closing). */
function serializeSubTreeNode(node: AppNode, indentLevel: number): string {
  const indent = '  '.repeat(indentLevel);
  const { data } = node;
  const subtreeId: string = data?.subtreeId ?? data?.type ?? 'UnnamedSubTree';
  const fields: NodeField[] = data?.fields ?? [];
  const nodeName: string | undefined = data?.nodeName;

  const attrs = buildAttributeString(fields, nodeName);
  return `${indent}<SubTree ID="${subtreeId}"${attrs}/>\n`;
}

/**
 * Build an XML attributes string from a node's fields and optional name.
 *
 * The `name` attribute is emitted first per BehaviorTree.cpp convention.
 * `output_key` values have their display braces stripped on export.
 */
function buildAttributeString(fields: NodeField[], nodeName?: string): string {
  const parts: string[] = [];

  if (nodeName) {
    parts.push(`name="${escapeXML(nodeName)}"`);
  }

  for (const field of fields) {
    let value = field.value;

    // output_key is stored with braces for display; strip them on export
    if (field.name === 'output_key' && typeof value === 'string') {
      value = value.replace(/^\{|\}$/g, '');
    }

    if (field.valueType === 'variable') {
      parts.push(`${field.name}="{${value}}"`);
    } else {
      parts.push(`${field.name}="${escapeXML(String(value))}"`);
    }
  }

  return parts.length > 0 ? ' ' + parts.join(' ') : '';
}

/**
 * Get the direct children of a node, sorted left-to-right by x-position.
 */
function getNodeChildren(nodeId: string, nodes: AppNode[], edges: AppEdge[]): AppNode[] {
  const childIds = edges.filter((e) => e.source === nodeId).map((e) => e.target);
  return childIds
    .map((id) => nodes.find((n) => n.id === id))
    .filter((n): n is AppNode => n !== undefined)
    .sort((a, b) => (a.position?.x ?? 0) - (b.position?.x ?? 0));
}

/**
 * Serialize the `<TreeNodesModel>` section that declares subtree port interfaces.
 */
function serializeTreeNodesModel(subtrees: Map<string, TreeData>): string {
  const withPorts: TreeData[] = [];
  subtrees.forEach((td) => {
    if (td.ports && td.ports.length > 0) withPorts.push(td);
  });
  if (withPorts.length === 0) return '';

  let xml = '  <!-- Subtree port definitions -->\n';
  xml += '  <TreeNodesModel>\n';

  for (const tree of withPorts) {
    xml += `    <SubTree ID="${escapeXML(tree.id)}">\n`;
    for (const port of tree.ports!) {
      const tag =
        port.direction === 'input' ? 'input_port' :
        port.direction === 'output' ? 'output_port' : 'inout_port';
      const defaultAttr = port.defaultValue ? ` default="${escapeXML(port.defaultValue)}"` : '';
      const typeAttr = port.type ? ` type="${port.type}"` : '';
      const reqAttr = port.required ? ' required="true"' : '';
      const desc = port.description ? escapeXML(port.description) : '';
      xml += `      <${tag} name="${escapeXML(port.name)}"${typeAttr}${defaultAttr}${reqAttr}>${desc}</${tag}>\n`;
    }
    xml += '    </SubTree>\n';
  }

  xml += '  </TreeNodesModel>\n\n';
  return xml;
}

