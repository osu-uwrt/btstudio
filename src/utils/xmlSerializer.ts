/**
 * XML serialization / deserialization for BehaviorTree.cpp v4 format.
 *
 * Converts between the editor's internal ReactFlow representation
 * (nodes + edges + variables) and the BT XML consumed by BehaviorTree.CPP.
 *
 * Public surface:
 *   Export:  exportToXML, exportMultiTreeToXML, exportSubtreeLibraryToXML
 *   Import:  importFromXML, importMultiTreeFromXML, importSubtreeLibraryFromXML
 *   Helpers: getReferencedSubtreeIds, updateSubtreeInXML
 *   Types:   TreeData, MultiTreeParseResult, MAIN_TREE_ID
 *
 * When editing this file, also update the corresponding tests in
 * src/test/xmlSerializer.test.ts.
 */

import { AppNode, AppEdge, NodeField, SubTreePort, Variable } from '../types';
import { getCategoryColor, inferCategoryFromType } from '../data/nodeLibrary';

// ---------------------------------------------------------------------------
// Serialization options
// ---------------------------------------------------------------------------

interface SerializationOptions {
  /** Pretty-print the output (default: true). */
  formatOutput?: boolean;
  /** Include generated-by comments (default: true). */
  includeComments?: boolean;
}

// ---------------------------------------------------------------------------
// Public types
// ---------------------------------------------------------------------------

/** A single BehaviorTree stored in a file. */
export interface TreeData {
  /** BehaviorTree ID attribute (e.g. "NavigationTree"). */
  id: string;
  nodes: AppNode[];
  edges: AppEdge[];
  variables: Variable[];
  /** Optional human-readable description (stored as an XML comment). */
  description?: string;
  /** Input/output ports for subtrees. */
  ports?: SubTreePort[];
  /** Optional custom color for subtree display (stored as an XML comment). */
  color?: string;
}

/** Sentinel ID that BehaviorTree.cpp uses for its main tree attribute. */
export const MAIN_TREE_ID = 'main_tree_to_execute';

/** Result of parsing a multi-tree XML file. */
export interface MultiTreeParseResult {
  mainTree: TreeData;
  subtrees: Map<string, TreeData>;
  /** Preserves the document order of trees in the file. */
  treeOrder: string[];
}

// ═══════════════════════════════════════════════════════════════════════════
// EXPORT (editor -> XML)
// ═══════════════════════════════════════════════════════════════════════════

/**
 * Export a single tree to BehaviorTree.cpp v4 XML.
 */
export function exportToXML(
  nodes: AppNode[],
  edges: AppEdge[],
  variables: Variable[],
  treeId: string = 'MainTree',
  options: SerializationOptions = {},
): string {
  const { formatOutput = true, includeComments = true } = options;

  const treeStructure = buildTreeStructure(nodes, edges);

  let xml = '<?xml version="1.0"?>\n';
  xml += `<root BTCPP_format="4" main_tree_to_execute="${escapeXML(treeId)}">\n`;

  if (includeComments) {
    xml += '  <!-- Generated by BTstudio -->\n\n';
  }

  xml += `  <BehaviorTree ID="${escapeXML(treeId)}">\n`;
  xml += serializeNodeRecursive(treeStructure.root, nodes, edges, variables, 2);
  xml += '  </BehaviorTree>\n';
  xml += '</root>';

  return formatOutput ? xml : xml.replace(/\n\s*/g, '');
}

/**
 * Export multiple trees (main + subtrees) to a single XML file.
 *
 * Used when saving a workspace file that embeds its referenced subtrees.
 */
export function exportMultiTreeToXML(
  mainTree: TreeData,
  subtrees: Map<string, TreeData>,
  options: SerializationOptions = {},
): string {
  const { formatOutput = true, includeComments = true } = options;

  let xml = '<?xml version="1.0"?>\n';
  xml += `<root BTCPP_format="4" main_tree_to_execute="${escapeXML(mainTree.id)}">\n`;

  if (includeComments) {
    xml += '  <!-- Generated by BTstudio -->\n\n';
  }

  // Main tree first
  xml += serializeBehaviorTree(mainTree);
  xml += '\n';

  // Then subtrees in insertion order
  subtrees.forEach((subtreeData) => {
    xml += serializeBehaviorTree(subtreeData);
    xml += '\n';
  });

  // Port definitions
  xml += serializeTreeNodesModel(subtrees);
  xml += '</root>';

  return formatOutput ? xml : xml.replace(/\n\s*/g, '');
}

/**
 * Export the workspace subtree library to XML.
 *
 * The library file has no `main_tree_to_execute` attribute because it
 * only contains reusable subtree definitions.
 */
export function exportSubtreeLibraryToXML(
  subtrees: Map<string, TreeData>,
  options: SerializationOptions = {},
): string {
  const { formatOutput = true, includeComments = true } = options;

  let xml = '<?xml version="1.0"?>\n';
  xml += '<root BTCPP_format="4">\n';

  if (includeComments) {
    xml += '  <!-- BTstudio Subtree Library -->\n\n';
  }

  subtrees.forEach((subtreeData) => {
    xml += serializeBehaviorTree(subtreeData);
    xml += '\n';
  });

  xml += serializeTreeNodesModel(subtrees);
  xml += '</root>';

  return formatOutput ? xml : xml.replace(/\n\s*/g, '');
}

