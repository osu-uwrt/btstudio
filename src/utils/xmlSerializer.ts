/**
 * Collect the IDs of all subtree nodes referenced in a flat node array.
 */
export function getReferencedSubtreeIds(nodes: AppNode[]): string[] {
  const seen = new Set<string>();
  for (const node of nodes) {
    if (node.data?.category === 'subtree') {
      const id = node.data.subtreeId || node.data.type;
      if (id) seen.add(id);
    }
  }
  return Array.from(seen);
}

/**
 * Patch a specific subtree inside an existing XML string.
 *
 * If the subtree already exists it is replaced in-place; otherwise a new
 * `<BehaviorTree>` element is appended before `</root>`.
 *
 * Used to efficiently propagate library changes to workspace files
 * without a full re-parse / re-serialize cycle.
 */
export function updateSubtreeInXML(
  xmlString: string,
  subtreeId: string,
  newSubtreeData: TreeData,
): string {
  const parser = new DOMParser();
  const xmlDoc = parser.parseFromString(xmlString, 'text/xml');
  const root = xmlDoc.querySelector('root');
  if (!root) return xmlString;

  // Locate existing element
  let targetElement: Element | null = null;
  root.querySelectorAll('BehaviorTree').forEach((bt) => {
    if (bt.getAttribute('ID') === subtreeId) {
      targetElement = bt;
    }
  });

  // Serialize the replacement content
  const structure = buildTreeStructure(newSubtreeData.nodes, newSubtreeData.edges);
  const newContent = serializeNodeRecursive(
    structure.root,
    newSubtreeData.nodes,
    newSubtreeData.edges,
    newSubtreeData.variables,
    2,
  );

  if (targetElement !== null) {
    (targetElement as Element).innerHTML = newContent;
  } else {
    const newBT = xmlDoc.createElement('BehaviorTree');
    newBT.setAttribute('ID', subtreeId);
    newBT.innerHTML = newContent;
    root.appendChild(newBT);
  }

  // Re-serialize to string and tidy up formatting artefacts
  const serializer = new XMLSerializer();
  let result = serializer.serializeToString(xmlDoc);
  result = result.replace(/><BehaviorTree/g, '>\n\n  <BehaviorTree');
  result = result.replace(/<\/BehaviorTree><\/root>/g, '</BehaviorTree>\n</root>');
  return result;
}

