/**
 * Parse `<TreeNodesModel>` and attach port definitions to matching subtrees.
 */
function attachPortDefinitions(root: Element, subtrees: Map<string, TreeData>): void {
  const portDefs = parseTreeNodesModel(root);
  portDefs.forEach((ports, id) => {
    const subtree = subtrees.get(id);
    if (subtree) subtree.ports = ports;
  });
}

/**
 * Walk all trees and annotate SubTree reference nodes' fields with
 * `portDirection` from the port definitions in `<TreeNodesModel>` or
 * from the referenced subtree's TreeData.
 *
 * This ensures that after XML import, subtree instances display the
 * correct [IN]/[OUT] port labels in the editor.
 */
function annotateSubTreeNodeFields(
  trees: TreeData[],
  portDefinitions: Map<string, SubTreePort[]>,
  subtreeDataMap: Map<string, TreeData>,
): void {
  for (const tree of trees) {
    for (const node of tree.nodes) {
      if (node.data?.category !== 'subtree') continue;

      const subtreeId = node.data.subtreeId || node.data.type;
      if (!subtreeId) continue;

      // Resolve ports from TreeNodesModel first, then from subtree TreeData
      const ports =
        portDefinitions.get(subtreeId) ??
        subtreeDataMap.get(subtreeId)?.ports;
      if (!ports || ports.length === 0) continue;

      // Build a port-name -> direction map
      const portDirMap = new Map<string, 'input' | 'output'>();
      for (const port of ports) {
        // 'inout' ports are treated as input for display purposes
        portDirMap.set(port.name, port.direction === 'output' ? 'output' : 'input');
      }

      // Annotate existing fields with portDirection
      node.data.fields = node.data.fields.map(field => {
        const dir = portDirMap.get(field.name);
        if (dir) return { ...field, portDirection: dir };
        return field;
      });

      // Attach ports to the node data for UI consistency
      node.data.ports = ports;
    }
  }
}

/**
 * Parse the `<TreeNodesModel>` section for subtree port definitions.
 */
function parseTreeNodesModel(root: Element): Map<string, SubTreePort[]> {
  const result = new Map<string, SubTreePort[]>();
  const model = root.querySelector('TreeNodesModel');
  if (!model) return result;

  for (const subtreeModel of Array.from(model.querySelectorAll('SubTree'))) {
    const subtreeId = subtreeModel.getAttribute('ID');
    if (!subtreeId) continue;

    const ports: SubTreePort[] = [
      ...parsePortElements(subtreeModel, 'input_port', 'input'),
      ...parsePortElements(subtreeModel, 'output_port', 'output'),
      ...parsePortElements(subtreeModel, 'inout_port', 'inout'),
    ];

    if (ports.length > 0) {
      result.set(subtreeId, ports);
    }
  }

  return result;
}

/**
 * Parse all port elements of a given tag name within a parent element.
 *
 * Shared by input_port, output_port, and inout_port parsing to avoid
 * duplicating the same attribute-extraction logic three times.
 */
function parsePortElements(
  parent: Element,
  tagName: string,
  direction: SubTreePort['direction'],
): SubTreePort[] {
  const ports: SubTreePort[] = [];

  for (const el of Array.from(parent.querySelectorAll(tagName))) {
    const name = el.getAttribute('name');
    if (!name) continue;

    ports.push({
      name,
      direction,
      type: (el.getAttribute('type') as SubTreePort['type']) || 'string',
      defaultValue: el.getAttribute('default') || undefined,
      description: el.textContent || undefined,
      required: el.getAttribute('required') === 'true',
    });
  }

  return ports;
}

