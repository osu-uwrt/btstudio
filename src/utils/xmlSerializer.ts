/**
 * Parse an XML string, validate it, and return the `<root>` element.
 */
function parseAndValidateRoot(xmlString: string): Element {
  const parser = new DOMParser();
  const xmlDoc = parser.parseFromString(xmlString, 'text/xml');

  const parseError = xmlDoc.querySelector('parsererror');
  if (parseError) {
    throw new Error('Invalid XML: ' + parseError.textContent);
  }

  const root = xmlDoc.querySelector('root');
  if (!root) {
    throw new Error('Invalid BehaviorTree XML: missing root element');
  }
  return root;
}

/**
 * Walk the children of `<root>` and map each `<BehaviorTree>` to the
 * preceding XML comments (if any), extracting description and BTstudio metadata.
 *
 * This is used to recover subtree descriptions and custom colors that are
 * stored as XML comments rather than attributes.
 */
function buildDescriptionMap(root: Element): Map<string, string> {
  const map = new Map<string, string>();
  let currentDescription: string | null = null;

  // nodeType 8 = COMMENT_NODE, 1 = ELEMENT_NODE
  root.childNodes.forEach((child) => {
    if (child.nodeType === 8) {
      const text = (child.textContent ?? '').trim();
      // Only track user description comments (skip generated and BTstudio meta)
      if (!isGeneratedComment(text) && !isBTstudioMetaComment(text)) {
        currentDescription = text;
      }
    } else if (child.nodeType === 1 && (child as Element).tagName === 'BehaviorTree') {
      const treeId = (child as Element).getAttribute('ID');
      if (treeId && currentDescription) {
        map.set(treeId, currentDescription);
      }
      currentDescription = null;
    } else if (child.nodeType === 1) {
      currentDescription = null;
    }
  });

  return map;
}

/**
 * Walk the children of `<root>` and extract BTstudio metadata comments
 * (e.g., `BTstudio:color=#FF5722`) preceding each `<BehaviorTree>` element.
 *
 * Returns a map from tree ID to color hex string.
 */
function buildColorMap(root: Element): Map<string, string> {
  const map = new Map<string, string>();
  const pendingColors: string[] = [];

  root.childNodes.forEach((child) => {
    if (child.nodeType === 8) {
      const text = (child.textContent ?? '').trim();
      const colorMatch = text.match(/^BTstudio:color=(.+)$/);
      if (colorMatch) {
        pendingColors.push(colorMatch[1]);
      }
    } else if (child.nodeType === 1 && (child as Element).tagName === 'BehaviorTree') {
      const treeId = (child as Element).getAttribute('ID');
      if (treeId && pendingColors.length > 0) {
        map.set(treeId, pendingColors[pendingColors.length - 1]);
      }
      pendingColors.length = 0;
    } else if (child.nodeType === 1) {
      pendingColors.length = 0;
    }
  });

  return map;
}

/** Returns true for comments generated by BTstudio that are NOT user descriptions. */
function isGeneratedComment(text: string): boolean {
  const lower = text.toLowerCase();
  return (
    lower.includes('generated by') ||
    lower.includes('subtree library') ||
    lower.includes('port definitions')
  );
}

/** Returns true for BTstudio metadata comments (e.g., color). */
function isBTstudioMetaComment(text: string): boolean {
  return text.startsWith('BTstudio:');
}

/**
 * Parse a single `<BehaviorTree>` element into a TreeData object.
 */
function parseBehaviorTreeElement(
  btElement: Element,
  treeId: string,
  description: string | undefined,
  color: string | undefined,
): TreeData {
  const variables: Variable[] = [];
  const firstSetBlackboardPerVariable = new Map<string, Element>();

  // Scan for SetBlackboard nodes to identify variable declarations
  collectSetBlackboardNodes(btElement, firstSetBlackboardPerVariable);

  const { nodes, edges } = parseTreeStructure(btElement, variables, firstSetBlackboardPerVariable);
  return { id: treeId, nodes, edges, variables, description, color };
}

/**
 * Recursively collect the first SetBlackboard element for each output_key.
 * The first occurrence is treated as a variable declaration.
 */
function collectSetBlackboardNodes(element: Element, map: Map<string, Element>): void {
  if (element.tagName === 'SetBlackboard') {
    const key = element.getAttribute('output_key');
    if (key && !map.has(key)) {
      map.set(key, element);
    }
  }
  for (const child of Array.from(element.children)) {
    collectSetBlackboardNodes(child, map);
  }
}

/**
 * Parse the children of a `<BehaviorTree>` element into ReactFlow nodes and edges.
 *
 * Always creates a synthetic Root node at the top of the tree.
 */
function parseTreeStructure(
  btElement: Element,
  variables: Variable[],
  firstSetBlackboardPerVariable: Map<string, Element>,
): { nodes: AppNode[]; edges: AppEdge[] } {
  const nodes: AppNode[] = [];
  const edges: AppEdge[] = [];

  // Synthetic root node
  const rootNode: AppNode = {
    id: 'root_node',
    type: 'btNode',
    position: { x: 250, y: 50 },
    data: {
      id: 'root',
      type: 'Root',
      category: 'root',
      name: 'Root',
      description: 'Root node of the behavior tree',
      fields: [],
      instanceId: 'root_node',
      color: getCategoryColor('root'),
    },
  };
  nodes.push(rootNode);

  const firstChild = btElement.children[0];
  if (firstChild) {
    const result = parseNodeRecursive(
      firstChild,
      250, // xPosition
      150, // yPosition
      0,   // depth
      variables,
      firstSetBlackboardPerVariable,
    );

    if (result.node) {
      nodes.push(result.node);
      // Add descendant nodes (skip the duplicate root already added)
      nodes.push(...result.allNodes.filter((n) => n.id !== result.node!.id));
      edges.push({
        id: `root_node-${result.node.id}`,
        source: 'root_node',
        target: result.node.id,
      });
      edges.push(...result.edges);
    }
  }

  return { nodes, edges };
}

/**
 * Recursively parse an XML element into a ReactFlow node and its descendant tree.
 */
function parseNodeRecursive(
  element: Element,
  x: number,
  y: number,
  depth: number,
  variables: Variable[],
  firstSetBlackboardPerVariable: Map<string, Element>,
): {
  node: AppNode | null;
  edges: AppEdge[];
  allNodes: AppNode[];
} {
  const edges: AppEdge[] = [];
  const allNodes: AppNode[] = [];
  let nodeType = element.tagName;

  // SubTree nodes have their own parsing path
  if (nodeType === 'SubTree') {
    const subtreeId = element.getAttribute('ID') || 'UnnamedSubTree';
    const node = createSubTreeNode(element, subtreeId, x, y);
    return { node, edges, allNodes: [node] };
  }

  // Detect DeclareVariable (first SetBlackboard per output_key)
  let isDeclareVariable = false;
  if (nodeType === 'SetBlackboard') {
    const outputKey = element.getAttribute('output_key');
    if (outputKey && firstSetBlackboardPerVariable.get(outputKey) === element) {
      isDeclareVariable = true;
      nodeType = 'DeclareVariable';
      variables.push({ name: outputKey, value: element.getAttribute('value') ?? '' });
    }
  }

  const nodeId = `${nodeType}_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
  const category = inferCategoryFromType(isDeclareVariable ? 'SetBlackboard' : nodeType);
  const nodeName = element.getAttribute('name') || undefined;

  // Parse XML attributes into NodeField[]
  const fields = parseElementAttributes(element, nodeType);

  const node: AppNode = {
    id: nodeId,
    type: 'btNode',
    position: { x, y },
    data: {
      id: nodeType.toLowerCase(),
      type: nodeType,
      category,
      name: isDeclareVariable ? 'Declare Variable' : nodeType,
      description: isDeclareVariable
        ? 'Declares a variable with an initial value (first assignment)'
        : `${nodeType} node`,
      fields,
      nodeName,
      instanceId: nodeId,
      color: getCategoryColor(category),
    },
  };
  allNodes.push(node);

  // Recursively parse children
  const children = Array.from(element.children);
  const childSpacing = 200;
  const totalWidth = children.length * childSpacing;
  let childX = x - totalWidth / 2 + childSpacing / 2;
  const childY = y + 100;

  for (const child of children) {
    const result = parseNodeRecursive(child, childX, childY, depth + 1, variables, firstSetBlackboardPerVariable);
    if (result.node) {
      edges.push({
        id: `${nodeId}-${result.node.id}`,
        source: nodeId,
        target: result.node.id,
      });
      allNodes.push(...result.allNodes);
      edges.push(...result.edges);
    }
    childX += childSpacing;
  }

  return { node, edges, allNodes };
}

/**
 * Parse XML attributes of an element into a NodeField array.
 *
 * Skips the `ID` and `name` attributes (handled separately).
 * Detects `{varName}` references and auto-parses numbers / booleans.
 */
function parseElementAttributes(element: Element, nodeType: string): NodeField[] {
  const fields: NodeField[] = [];
  const isBlackboardType = nodeType === 'SetBlackboard' || nodeType === 'DeclareVariable';

  for (const attr of Array.from(element.attributes)) {
    if (attr.name === 'ID' || attr.name === 'name') continue;

    const raw = attr.value;
    let valueType: NodeField['valueType'] = 'literal';
    let parsedValue: string | number | boolean = raw;

    // Detect {varName} references
    const varMatch = raw.match(/^\{(.+)\}$/);
    if (varMatch) {
      valueType = 'variable';
      parsedValue = varMatch[1];
    } else if (!isNaN(Number(raw))) {
      parsedValue = Number(raw);
    } else if (raw === 'true' || raw === 'false') {
      parsedValue = raw === 'true';
    }

    // output_key for blackboard nodes gets display braces
    const displayValue =
      attr.name === 'output_key' && isBlackboardType
        ? `{${parsedValue}}`
        : parsedValue;

    fields.push({
      name: attr.name,
      type: typeof parsedValue === 'number' ? 'number' : typeof parsedValue === 'boolean' ? 'boolean' : 'string',
      valueType,
      value: displayValue,
    });
  }

  return fields;
}

/** Create a SubTree reference node from an XML element. */
function createSubTreeNode(element: Element, subtreeId: string, x: number, y: number): AppNode {
  const nodeId = `SubTree_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
  const nodeName = element.getAttribute('name') || undefined;

  // Port mappings come from XML attributes (excluding ID and name)
  const fields = parseElementAttributes(element, 'SubTree');

  return {
    id: nodeId,
    type: 'btNode',
    position: { x, y },
    data: {
      id: subtreeId.toLowerCase(),
      type: subtreeId,
      category: 'subtree',
      name: subtreeId,
      description: `SubTree: ${subtreeId}`,
      fields,
      subtreeId,
      nodeName,
      instanceId: nodeId,
      color: getCategoryColor('subtree'),
    },
  };
}

