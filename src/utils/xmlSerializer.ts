/**
 * Import a single-tree BehaviorTree.cpp XML string (convenience wrapper).
 *
 * For files that contain multiple trees, use `importMultiTreeFromXML`.
 */
export function importFromXML(xmlString: string): {
  nodes: AppNode[];
  edges: AppEdge[];
  variables: Variable[];
} {
  const result = importMultiTreeFromXML(xmlString);
  return {
    nodes: result.mainTree.nodes,
    edges: result.mainTree.edges,
    variables: result.mainTree.variables,
  };
}

/**
 * Import a multi-tree BehaviorTree.cpp XML file.
 *
 * The main tree is identified by:
 *   1. Matching `main_tree_to_execute` attribute.
 *   2. First `<BehaviorTree>` element if the attribute is absent.
 *   3. Fallback: tree with ID equal to `MAIN_TREE_ID`.
 */
export function importMultiTreeFromXML(xmlString: string): MultiTreeParseResult {
  const root = parseAndValidateRoot(xmlString);
  const mainTreeId = root.getAttribute('main_tree_to_execute');
  const behaviorTrees = root.querySelectorAll('BehaviorTree');

  if (behaviorTrees.length === 0) {
    throw new Error('Invalid BehaviorTree XML: missing BehaviorTree element');
  }

  const descriptionMap = buildDescriptionMap(root);
  const colorMap = buildColorMap(root);
  const subtrees = new Map<string, TreeData>();
  const treeOrder: string[] = [];
  let mainTree: TreeData | null = null;

  behaviorTrees.forEach((btElement, index) => {
    const treeId = btElement.getAttribute('ID') || `Tree_${index}`;
    const description = descriptionMap.get(treeId) || btElement.getAttribute('description') || undefined;
    const color = colorMap.get(treeId) || undefined;
    treeOrder.push(treeId);

    const isMainTree =
      (mainTreeId && treeId === mainTreeId) ||
      (!mainTreeId && index === 0) ||
      treeId === MAIN_TREE_ID;

    const treeData = parseBehaviorTreeElement(btElement, treeId, description, color);

    if (isMainTree && !mainTree) {
      mainTree = treeData;
    } else {
      subtrees.set(treeId, treeData);
    }
  });

  if (!mainTree) {
    throw new Error('Failed to parse main tree');
  }

  // Attach port definitions from <TreeNodesModel>
  attachPortDefinitions(root, subtrees);

  // Annotate SubTree reference nodes' fields with portDirection from port defs
  const portDefs = parseTreeNodesModel(root);
  const allTrees = [mainTree!, ...Array.from(subtrees.values())];
  annotateSubTreeNodeFields(allTrees, portDefs, subtrees);

  return { mainTree, subtrees, treeOrder };
}

/**
 * Import a subtree library XML file.
 *
 * Returns a map from subtree ID to its TreeData.
 * Returns an empty map for invalid / empty library files.
 */
export function importSubtreeLibraryFromXML(xmlString: string): Map<string, TreeData> {
  const parser = new DOMParser();
  const xmlDoc = parser.parseFromString(xmlString, 'text/xml');

  if (xmlDoc.querySelector('parsererror')) {
    throw new Error('Invalid XML: ' + xmlDoc.querySelector('parsererror')!.textContent);
  }

  const root = xmlDoc.querySelector('root');
  if (!root) return new Map();

  const descriptionMap = buildDescriptionMap(root);
  const colorMap = buildColorMap(root);
  const behaviorTrees = root.querySelectorAll('BehaviorTree');
  const subtrees = new Map<string, TreeData>();

  behaviorTrees.forEach((btElement, index) => {
    const treeId = btElement.getAttribute('ID') || `SubTree_${index}`;
    const description = descriptionMap.get(treeId) || btElement.getAttribute('description') || undefined;
    const color = colorMap.get(treeId) || undefined;
    const treeData = parseBehaviorTreeElement(btElement, treeId, description, color);
    subtrees.set(treeId, treeData);
  });

  attachPortDefinitions(root, subtrees);

  // Annotate SubTree reference nodes' fields with portDirection from port defs
  const portDefs = parseTreeNodesModel(root);
  const allTrees = Array.from(subtrees.values());
  annotateSubTreeNodeFields(allTrees, portDefs, subtrees);

  return subtrees;
}

